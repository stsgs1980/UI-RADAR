export function getToolById(id: string): UITool | undefined {
  return UI_TOOLS.find(tool => tool.id === id);
}

export function getToolsByCategory(category: ToolCategory): UITool[] {
  return UI_TOOLS.filter(tool => tool.category === category);
}

export function getToolsByIds(ids: string[]): UITool[] {
  return ids.map(id => getToolById(id)).filter((tool): tool is UITool => tool !== undefined);
}

export function getRadarInfo(toolId: string): TechRadarItem | undefined {
  return TECH_RADAR_ITEMS.find(item => item.toolId === toolId);
}
